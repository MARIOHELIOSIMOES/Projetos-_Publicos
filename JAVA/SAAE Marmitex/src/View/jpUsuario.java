    private void SalvarUsuario(){
        String senhaA, senhaN;
        
        StringBuilder sb = new StringBuilder();
        for(char a : txfSenhaAtual.getPassword()){
            sb.append(a);
        }
        senhaA = sb.toString();
        
        sb = new StringBuilder();
        for(char a : txfSenhaNova.getPassword()){
            sb.append(a);
        }
        senhaN = sb.toString().trim();
        
        
        Usuario u = this.usuario;
        if(senhaA.equals(usuario.getSenha())){
            u.setSenha(senhaN);
            new UsuarioDAO().Alterar(u);
             JOptionPane.showMessageDialog(null,"Alterado com sucesso!!","", JOptionPane.INFORMATION_MESSAGE);
        }else if(senhaA.equals("saaenovo")){
            u.setNome(txfUsuario.getText().trim());
            u.setSenha(senhaN);
            new UsuarioDAO().Inserir(u);
             JOptionPane.showMessageDialog(null,"Novo usu√°rio inserido!!","", JOptionPane.INFORMATION_MESSAGE);
        }else{
            JOptionPane.showMessageDialog(null,"Senha Incorreta!!","", JOptionPane.WARNING_MESSAGE);
        }
        
    }

